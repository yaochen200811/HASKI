def fetch_knowledge(question):
    """
    Initiate asynchronous knowledge retrieval.

    This function sends the user's question to the Knowledge Service, which
    begins retrieving relevant external information in the background.
    Generation continues immediately without waiting for retrieval to finish.

    Parameters
    ----------
    question : str
        User input question.

    Returns
    -------
    str
        Unique identifier used to poll the Knowledge Service for completion.
    """
    url = f"{API_BASE_KNOWLEDGE}/fetch-knowledge"
    response = requests.post(url, json={"question": question})
    if response.status_code == 200:
        return response.json()["id"]
    else:
        raise Exception(f"Failed to start fetch: {response.text}")

def check_knowledge_ready(knowledge_id):
    """
    Check whether asynchronous knowledge retrieval has completed.

    When retrieval is complete, the returned knowledge dictionary is converted
    into a formatted text block that can later be appended directly to the
    model context for knowledge injection.

    Parameters
    ----------
    knowledge_id : str
        Identifier returned by fetch_knowledge().

    Returns
    -------
    tuple(bool, Optional[str])

        (False, None)
            Retrieval is still running.

        (True, knowledge_text)
            Retrieval completed successfully.
    """
    url = f"{API_BASE_KNOWLEDGE}/check-knowledge"
    response = requests.get(url, params={"id": knowledge_id})
    
    if response.status_code != 200:
        raise Exception(f"Status check failed: {response.text}")
    
    data = response.json()
    status = data.get("status")

    if status == "ready":
        knowledge_dict = data.get("knowledge", {})
        # Convert the dict into a readable string
        if len(knowledge_dict) == 0:
            return True, ""
        knowledge_text = "Trust following Knowledge:\n"
        knowledge_text += "\n".join(
            f"{topic}: {description}" for topic, description in knowledge_dict.items()
        )
        return True, knowledge_text
    elif status == "error":
        raise Exception(f"Knowledge fetch error: {data.get('error')}")
    else:
        return False, None
    
def check_good_timing(question, output, attention):
    """
    Query the IPM Service to determine whether the current
    reasoning state is suitable for knowledge injection.

    The timing model receives the original question, the partially generated
    reasoning, and summarized attention statistics to predict whether external
    knowledge can be injected without disrupting the model's reasoning process.

    Parameters
    ----------
    question : str
        Original user question.

    output : str
        Current partial response generated by the reasoning model.

    attention : list
        Aggregated attention statistics extracted from generated paragraphs.

    Returns
    -------
    str
        "True" if knowledge should be injected at the current step;
        otherwise "False".
    """
    r = requests.post(F"{API_BASE_TIMING}/timing", json={"question": question, "output": output, "attention": attention})
    r.raise_for_status()
    data = r.json()
    return data.get("timing", "")

